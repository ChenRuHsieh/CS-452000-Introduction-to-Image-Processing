clc;
clear all;
close all;
input = imread('Fig0441(a)(characters_test_pattern).tif'); % 讀取題目圖片
input = imresize(input, 128/688); % 縮小成128x128
subplot(1,3,1);imshow(input);title('Origin的1/10');
input = im2single(input);
imwrite(im2uint8(input),'input.tif');
[M,N] = size(input);
input_shift = fshift(input); % 做(-1)處理
n=1;    
    for i = [60, 160]
        n=n+1;    
        tic
        D0 = i*128/688; % 因為圖片有縮小，也對D0做縮小
        MyGHPF = myGHPF(i,M,N);
        outputDFT = myDFT2(input_shift);
        G = outputDFT .* MyGHPF;
        GIDFT = real(myIDFT2(G));
        GIDFT = fshift(GIDFT); %做(-1)處理
        GIDFT = powerlawTransform(GIDFT, 0.3); % 太暗，做亮化處理
        subplot(1,3,n);imshow(GIDFT);
        switch i 
        case 60
            title('D0 = 60');
            imwrite(im2uint8(GIDFT),'D0 = 60_light.tif');
            fprintf("D0 = 60 的GLPH已完成");
        case 160 
            title('D0 = 160');
            imwrite(im2uint8(GIDFT),'D0 = 160_light.tif');
            fprintf("D0 = 160 的GLPH已完成");
        end
        toc
    end
